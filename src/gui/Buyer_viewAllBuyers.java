    public void loadTable(DefaultTableModel dfm){
        dfm.setRowCount(0);
        try {
            ResultSet orders=MySQL.execute("SELECT * FROM `buyer`");
            while(orders.next()){
                Vector v=new Vector();
                v.add(orders.getInt("buyer_id"));
                v.add(orders.getString("name"));
                v.add(orders.getString("mobile"));
                v.add(orders.getString("address"));
                
                dfm.addRow(v);
            }
        } catch (Exception e) {
//            e.printStackTrace();
              log1.warning(e.toString());
        }
    }
